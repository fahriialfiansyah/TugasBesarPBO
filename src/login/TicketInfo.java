    //insert flight data
    private void save() {
        String flightCode = f_flightCode.getText().trim();
        String passport = f_passport.getText().trim();
        String gender = select_gender.getSelectedItem().toString();
        String nationality = f_nationality.getText().trim();
        //validate
        if (passport.equals("")
                || gender.equals("[ Select ]")
                || nationality.equals("")) {
            Flights.message("Please fill all the fields!");
        } else {
            Object options[] = {"YES, Save now!", "NO, Not now!"};
            int x = JOptionPane.showOptionDialog(this, "Are you sure you want to save (YES/NO)?", "Message",
                    JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, null);
            if (x == JOptionPane.YES_OPTION) {
                flights.setFlightCode(flightCode);
                flights.setPassportNumber(passport);
                flights.setGender(gender);
                flights.setNationality(nationality);
                flights._insert();
                clear();
                this.dispose();
                Booking booking = new Booking();
                booking.setVisible(true);
            }
        }
    }
