    private void autonumber() {
        try {
            Connection c = DBConnection.getCon();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM flight_ticket ORDER BY flightCode DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("flightCode").substring(3);
                String ID = "" + (Integer.parseInt(NoID) + 1);
                String Zero = "";

                if (ID.length() == 1) {
                    Zero = "00";
                } else if (ID.length() == 2) {
                    Zero = "0";
                } else if (ID.length() == 3) {
                    Zero = "";
                }

                f_flightCode.setText("AIR" + Zero + ID);
            } else {
                f_flightCode.setText("AIR001");
            }
            r.close();
            s.close();
        } catch (NumberFormatException | SQLException e) {
        }
    }

